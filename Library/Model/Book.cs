namespace Library;

public class Book
{
    private int idBook { get; set; }
    private String isbn { get; set; }
    private String title { get; set; }
    private String author { get; set; }
    
    public Book()
    {
        this.idBook = idBook;
        this.isbn = isbn;
        this.author = author;
        this.title = title;

    }
    
}